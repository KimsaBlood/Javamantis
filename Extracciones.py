class Extracciones:

	def __init__(self,nombre,expresiones):
		self.nombre=nombre
		self.expresiones=expresiones

	def setNombre(self,nombre):
		self.nombre=nombre

	def setExpresiones(self,expresiones):
		self.expresiones=expresiones

	def getNombre(self):
		return self.nombre

	def getExpresiones(self):
		return self.expresiones